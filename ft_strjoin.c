#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len_tot;
	char	*p;
	int		i;
	int		j;

	i = 0;
	j = 0;
	len_tot = ft_strlen(s1) + ft_strlen(s2);
	p = malloc(sizeof (char) * (len_tot + 1));
	if (!p)
		return (NULL);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		p[i + j] = s2[j];
		j++;
	}
	p[i + j] = '\0';
	return (p);
}
